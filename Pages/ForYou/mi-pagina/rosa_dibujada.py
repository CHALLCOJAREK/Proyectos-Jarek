xs = [x for x, y in all_points]
ys = [y for x, y in all_points]

center_x = (max(xs) + min(xs)) / 2
center_y = (max(ys) + min(ys)) / 2

# Ajustes finales
scale = 0.7  # Cambia esto si quieres que se vea m치s grande o m치s peque침o

# Dibujar todos los contornos (sin invertir eje Y)
for path in contours:
    t.penup()
    x, y = path[0]
    t.goto((x - center_x) * scale, (y - center_y) * scale)
    t.pendown()
    t.begin_fill()
    for x, y in path[1:]:
        t.goto((x - center_x) * scale, (y - center_y) * scale)
    t.goto((path[0][0] - center_x) * scale, (path[0][1] - center_y) * scale)
    t.end_fill()
